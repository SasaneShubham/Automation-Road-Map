package Control_Statements;

public class Switch_Case2 {


	public static void main(String[] args) {


		switch ("SS") {


		case ("AD"):

			System.out.println("code is AD");	
		break;

		case ("YF"):

			System.out.println("code is YF");	
		break;

		case ("SS"):

			System.out.println("code is SS");	
		break;

		case ("UI"):

			System.out.println("code is UI");	
		break;

		case ("MW"):

			System.out.println("code is MW");	
		break;

		default:
			System.out.println("Invalid input");


		}
	}
}
